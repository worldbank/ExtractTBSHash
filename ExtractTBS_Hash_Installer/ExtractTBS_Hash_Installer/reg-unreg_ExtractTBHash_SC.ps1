param (
    [string]$installParam = ""
)

$installInstruction = $installParam.ToLowerInvariant();

$taskFolder = "\ACfB\"
$xmlPath = "C:\Program Files\The World Bank Group\Extract ACfB Signer Info\Create PublisherTBSHash event log - Trigger Event 3033.xml"
$taskName = (Get-Item $xmlPath).BaseName
$evtSourceName = "ACfB-WBG-Patch"

function ExtractTBSHash_Uninstall {

    # delete event source
    remove-eventlog -source "$evtSourceName" -ErrorAction SilentlyContinue

    # Disable to prevent future runs
    schtasks /change /tn $taskFolder$taskName /disable 

    # End the task
    schtasks /end /tn $taskFolder$taskName | Out-Null

    # Poll up to 20 times (1 per second)
    for ($i = 0; $i -lt 20; $i++) {
        Start-Sleep -Seconds 1
        $status = schtasks /query /tn $taskFolder$taskName /fo LIST /v | Select-String "^Status:" | ForEach-Object {
            ($_ -split ":\s*", 2)[1].Trim()
        }

        if ($status -ne "Running") {
            Write-Output "Task '$taskFolder$taskName' has stopped."
            break
        }
    }

    # If still running after loop
    if ($status -eq "Running") {
        Write-Output "Timeout: Task '$taskFolder$taskName' is still running after 20 seconds."
    }

    # Unregister task with the same name
    schtasks /delete /tn $taskFolder$taskName /f
}

function ExtractTBSHash_Install {
    # Create Event Source
    New-EventLog -LogName Application -Source "$evtSourceName" -ErrorAction SilentlyContinue
    # Register the scheduled task using schtasks.exe
    schtasks /create /tn $taskFolder$taskName /xml $xmlPath /ru "SYSTEM"
}

# INSTALLATION ROUTINE - SCRIPT STARTS HERE
switch ($installInstruction) {
    "install"   { ExtractTBSHash_Uninstall; ExtractTBSHash_Install; exit }
    "uninstall" { ExtractTBSHash_Uninstall; exit }
    default     { Write-Host "No installation instruction supplied. Exiting." }
}


